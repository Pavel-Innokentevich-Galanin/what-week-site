export default function getStringMonth(month: number) {
  const ru = [
    'Студзень (ЯНВ)',
    'Люты (ФЕВ)',
    'Сакавiк (МАР)',
    'Красавiк (АПР)',
    'ТРАВЕНЬ (МАЙ)',
    'Чэрвень (ИЮН)',
    'Лiпень (ИЮЛ)',
    'Жнiвень (АВГ)',
    'Вераснь (СЕН)',
    'Кастрычнiк (ОКТ)',
    'Лiстапад (НОЯ)',
    'Снежань (ДЕК)',
  ];
  return ru[month - 1];
}
